//al presionar el botón repetir el pedido de número hasta que ingresemos el 9.
function mostrar()
{
	let i;
	let repeticiones;

	for(i=1;i>0;i++)
	{
		repeticiones=parseInt(prompt("Ingrese un numero"));

		if(repeticiones==9){
			break;
		}
	}

}//FIN DE LA FUNCIÓN